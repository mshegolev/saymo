"""Reading text for voice sample recording (~5 minutes of natural Russian speech).

Generates text via Ollama on the fly. Falls back to a static text if Ollama is unavailable.
"""

import logging

import httpx

logger = logging.getLogger("saymo.reading_text")

GENERATE_PROMPT = """\
Сгенерируй текст для чтения вслух на русском языке длительностью примерно 5 минут.

ТРЕБОВАНИЯ:
- Текст должен быть похож на рабочие стендап-отчёты и обсуждения в IT-команде.
- Включи разнообразную лексику: IT-термины (деплой, пайплайн, ревью, автотесты, \
мониторинг, SLA, CI/CD), числа (проценты, миллисекунды, количества), \
названия технологий (Kubernetes, PostgreSQL, Grafana, Kafka, Docker), \
обычную разговорную речь.
- Раздели на 10 абзацев по 4-5 предложений каждый.
- Пиши от первого лица, как будто рассказываешь коллегам.
- Каждый абзац — отдельная тема (разные задачи, проекты, события).
- Числа пиши СЛОВАМИ (не цифрами): "сто двадцать миллисекунд", "семьдесят пять процентов".
- Разделяй абзацы ДВУМЯ переводами строки.
- НЕ нумеруй абзацы. НЕ добавляй заголовки.
- Стиль — живой, разговорный, естественный.
"""


def generate_paragraphs(
    ollama_url: str = "http://localhost:11434",
    model: str = "qwen2.5-coder:7b",
) -> list[str]:
    """Generate reading text via Ollama. Falls back to static text on failure."""
    try:
        logger.info(f"Generating reading text via Ollama ({model})")
        client = httpx.Client(timeout=120.0, proxy=None)
        response = client.post(
            f"{ollama_url}/api/generate",
            json={
                "model": model,
                "prompt": GENERATE_PROMPT,
                "stream": False,
                "options": {
                    "temperature": 0.9,
                    "num_predict": 2000,
                },
            },
        )
        response.raise_for_status()
        text = response.json().get("response", "").strip()
        paragraphs = [p.strip() for p in text.split("\n\n") if p.strip()]
        if len(paragraphs) >= 5:
            return paragraphs
        logger.warning(f"Ollama returned only {len(paragraphs)} paragraphs, using static text")
    except Exception as e:
        logger.warning(f"Ollama unavailable ({e}), using static text")

    return FALLBACK_PARAGRAPHS


