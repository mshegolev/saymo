# All prompts combined for training session
ALL_PROMPTS = STANDUP_PROMPTS + QA_PROMPTS + GENERAL_PROMPTS + IT_PROMPTS


def get_prompts(category: str | None = None) -> list[str]:
    """Get training prompts by category.

    Args:
        category: One of 'standup', 'qa', 'general', 'it', or None for all.

    Returns:
        List of prompt sentences.
    """
    categories = {
        "standup": STANDUP_PROMPTS,
        "qa": QA_PROMPTS,
        "general": GENERAL_PROMPTS,
        "it": IT_PROMPTS,
    }
    if category is None:
        return ALL_PROMPTS
    if category not in categories:
        raise ValueError(f"Unknown category: {category}. Choose from: {list(categories.keys())}")
    return categories[category]
